package org.example.osadnici;

/**
 * Enum representing different types of pawns used in the game.
 * Pawns can be roads, villages, or towns.
 */
public enum PawnType {
    /** Represents a road pawn. */
    Road,
    /** Represents a village pawn. */
    Village,
    /** Represents a town pawn. */
    Town
}
