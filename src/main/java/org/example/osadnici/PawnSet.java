package org.example.osadnici;

/**
 * Represents a set of pawns used in the game.
 */
public class PawnSet {
    /** The number of pawns in the set. */
    public int numberOfPawns;

    /**
     * Constructs a PawnSet with the specified number of pawns.
     *
     * @param numberOfPawns the number of pawns in the set
     */
    public PawnSet(int numberOfPawns) {
        this.numberOfPawns = numberOfPawns;
    }
}
